// Quick stats computed from the sample data
export function getQuickStats() {
  const total = SAMPLE_PROJECTS.length
  const totalCapacity = SAMPLE_PROJECTS.reduce((sum, p) => sum + p.capacity_mw, 0)
  const totalStorage = SAMPLE_PROJECTS.reduce((sum, p) => sum + (p.storage_mwh || 0), 0)
  const operating = SAMPLE_PROJECTS.filter((p) => p.status === 'operating').length
  const construction = SAMPLE_PROJECTS.filter((p) => p.status === 'construction').length
  const development = SAMPLE_PROJECTS.filter((p) => p.status === 'development').length
  const byTech = {
    wind: SAMPLE_PROJECTS.filter((p) => p.technology === 'wind').length,
    solar: SAMPLE_PROJECTS.filter((p) => p.technology === 'solar').length,
    bess: SAMPLE_PROJECTS.filter((p) => p.technology === 'bess').length,
    hybrid: SAMPLE_PROJECTS.filter((p) => p.technology === 'hybrid').length,
  }
  const states = [...new Set(SAMPLE_PROJECTS.map((p) => p.state))]

  return { total, totalCapacity, totalStorage, operating, construction, development, byTech, states }
}
