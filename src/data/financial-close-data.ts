/**
 * Financial Close (FC) / Final Investment Decision (FID) Tracker
 *
 * Tracks which CIS and LTESA projects have reached financial close,
 * a critical milestone that confirms development is complete and
 * construction financing has been secured.
 *
 * Data sources:
 * - Developer press releases and websites
 * - RenewEconomy, pv magazine, Energy-Storage.News
 * - ARENA, CEFC announcements
 * - ASX disclosures (for listed developers)
 * - Legal adviser deal announcements (Allens, HSF, etc.)
 *
 * Research date: March 2026
 */

export interface FinancialCloseProject {
  name: string
  projectId: string | null
  developer: string
  technology: 'wind' | 'solar' | 'bess' | 'hybrid' | 'pumped_hydro' | 'caes'
  scheme: 'CIS' | 'LTESA' | 'CIS+LTESA' | 'Non-scheme'
  round: string
  capacityMW: number
  storageMWh?: number
  state: string

  // Financial close details
  fcStatus: 'confirmed' | 'effective' | 'not_reached' | 'operating'
  fcDate?: string               // ISO date of FC/FID
  fcValueM?: number             // Total investment value in AUD millions
  fcDebtM?: number              // Debt financing component
  fcLenders?: string[]          // Key lenders/financiers
  arenaGrantM?: number          // ARENA grant if applicable
  cefcFundingM?: number         // CEFC funding if applicable

  // Construction details
  constructionStartDate?: string
  expectedCOD?: string
  epcContractor?: string
  oem?: string                  // Turbine or battery OEM

  // Current status
  currentStatus: 'operating' | 'commissioning' | 'construction' | 'pre-construction' | 'development' | 'approved'

  // Sources
  sourceUrl?: string
  sourceDescription?: string
  additionalSources?: string[]

  notes?: string
}

